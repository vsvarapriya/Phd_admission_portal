<h4>a. Eligibility Criteria for Ph.D. Programme in Engineering </h4>
Candidates with a Master’s degree in Engineering/Technology or a Master’s
degree by Research in Engineering/Technology in relevant branch with 60% (or
above) aggregate marks (CGPA ≥ 6.5/10) in both UG and PG, for admission
under UR/EWS/OBC-NCL category, and 55% aggregate (or above) marks or
equivalent CGPA of ≥ 6.0 for SC/ST/PwD candidates in both UG and PG. They
should have qualified GATE or NET in the relevant disciplines.
<h4>b. Eligibility Criteria for Ph.D. Programme in Sciences and Humanities</h4>
Master’s degree in Sciences/Humanities in both UG and PG in relevant branch
of Science/Humanities with 60% (or above) aggregate marks (CGPA ≥ 6.5/10) in
both UG and PG, for admission under UR/EWS/OBC-NCL category, and 55%
aggregate (or above) marks or equivalent CGPA of ≥ 6.0 for SC/ST/PwD
candidates in both UG and PG. Candidates should have qualified GATE or
UGC/CSIR-NET in relevant discipline.
